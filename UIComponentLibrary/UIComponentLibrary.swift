import Foundation

public final class UIComponentLibrary {
    
    let name = "UIComponentLibrary"
    
    public func add(a: Int, b: Int) -> Int {
        return a + b
    }
    
    public func sub(a: Int, b: Int) -> Int {
        return a - b
    }
    
}
